public class TaiLieuChiTiet {
	private String maVachSach, maTaiLieu, ngayCapNhat, maNguoiDungCapNhat, tinhTrang, danhDauXuLy;

	public TaiLieuChiTiet(String maVachSach, String maTaiLieu, String ngayCapNhat, String maNguoiDungCapNhat,
			String tinhTrang, String danhDauXuLy) {
		super();
		this.maVachSach = maVachSach;
		this.maTaiLieu = maTaiLieu;
		this.ngayCapNhat = ngayCapNhat;
		this.maNguoiDungCapNhat = maNguoiDungCapNhat;
		this.tinhTrang = tinhTrang;
		this.danhDauXuLy = danhDauXuLy;
	}
	
	
}
