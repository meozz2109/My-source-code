public class NguoiDung {
	private String userName, hoTen, namSinh, gioiTinh, email, dienThoai, maNguoiDung;

	public NguoiDung(String userName, String hoTen, String namSinh, String gioiTinh, String email, String dienThoai,
			String maNguoiDung) {
		super();
		this.userName = userName;
		this.hoTen = hoTen;
		this.namSinh = namSinh;
		this.gioiTinh = gioiTinh;
		this.email = email;
		this.dienThoai = dienThoai;
		this.maNguoiDung = maNguoiDung;
	}
	
	
}
